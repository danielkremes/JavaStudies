package student.testsClass;

/**
 * Class that defines the possible status of a person's age.
 * This includes:
 * - ADULT: A person who is 18 years or older.
 * - UNDERAGE: A person who is younger than 18 years.
 * - ELDERLY: A person who is 60 years or older.
 */
public class AdultStatus {

    /**
     * Represents the status of an adult person.
     */
    public static String ADULT = "adult";

    /**
     * Represents the status of an underage person.
     */
    public static String UNDERAGE = "underage";

    /**
     * Represents the status of an elderly person.
     */
    public static String ELDERLY = "elder";
}
