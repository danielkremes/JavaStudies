package student.utilities;

/**
 * This class defines constant status values for students.
 * It represents the possible academic statuses of a student:
 * - APPROVED
 * - REPROVED
 * - RECUPERATION
 */
public class StudentStatus {
    public static final String APPROVED = "Student Approved";
    public static final String REPROVED = "Student Reproved";
    public static final String RECUPERATION = "Student Recuperation";
}
